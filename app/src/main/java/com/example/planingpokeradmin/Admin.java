package com.example.planingpokeradmin;

public class Admin {
    String admiId;
    String adminName;

    public Admin(){

    }

    public Admin(String admiId, String adminName) {
        this.admiId = admiId;
        this.adminName = adminName;
    }

    public String getAdmiId() {
        return admiId;
    }

    public String getAdminName() {
        return adminName;
    }





}
